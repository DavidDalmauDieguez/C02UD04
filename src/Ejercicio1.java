// Declara dos variables numericas (con el alor que desees), muestra por consola la suma. resta, multiplicacion, división y modulo

public class Ejercicio1 {

	public static void main(String[] args) {
		// Creación de variable
		int a = 10;
		int b = 5;
		
		// Sumamos los operadores
		int sum = a + b;
		System.out.println("La suma de los dos valores = " + sum);
		
		// Restamos los operadores
		int resta = a - b;
		System.out.println("La resta de los dos valores = " + resta);
		
		// Multiplicamos los operadores
		int multiplicacion = a * b;
		System.out.println("La multiplicacion de los dos valores = " + multiplicacion);
		
		// Dividimos los operadores
		int division = a / b;
		System.out.println("La division de los dos valores = " + division);
		
		// Mostramos el resto de la division
		int resto = a % b;
		System.out.println("El resto de la divisoon = " + resto);
		
	}

}
